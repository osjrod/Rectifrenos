        public static DataTable ConvertirListaClientesToDataTable(IList datas)
        {

            PropertyDescriptorCollection properties = TypeDescriptor.GetProperties(typeof(Clientes.Cliente));

            DataTable table = new DataTable();

            foreach (PropertyDescriptor prop in properties) table.Columns.Add(prop.Name, Nullable.GetUnderlyingType(prop.PropertyType) ?? prop.PropertyType);

            foreach (Clientes.Cliente item in datas)
            {
                DataRow row = table.NewRow();
                foreach (PropertyDescriptor prop in properties) row[prop.Name] = prop.GetValue(item) ?? DBNull.Value;
                table.Rows.Add(row);
            }
            return table;
        }

